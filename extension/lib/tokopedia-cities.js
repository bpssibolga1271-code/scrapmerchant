/**
 * Look up Tokopedia fcity ID for a BPS region name.
 * Tries exact match first, then fuzzy matching by normalizing
 * common BPS naming conventions (e.g., "KABUPATEN DONGGALA" -> "Kab. Donggala").
 *
 * @param {string} regionName — BPS region name (may be uppercase)
 * @param {string} [provinceName] — province name for province-level fallback
 * @returns {string|null} — fcity numeric ID(s) or null
 */
function lookupTokopediaFcity(regionName, provinceName) {
  if (!regionName) return null;

  // 1. Exact match
  if (TOKOPEDIA_FCITY_MAP[regionName]) {
    return TOKOPEDIA_FCITY_MAP[regionName];
  }

  // 2. Title-case the name for matching
  const titleCased = regionName
    .toLowerCase()
    .split(' ')
    .map(w => w.charAt(0).toUpperCase() + w.slice(1))
    .join(' ');

  if (TOKOPEDIA_FCITY_MAP[titleCased]) {
    return TOKOPEDIA_FCITY_MAP[titleCased];
  }

  // 3. BPS uses "KABUPATEN X" or "KOTA X" — convert to "Kab. X" or city name
  const upper = regionName.toUpperCase().trim();
  if (upper.startsWith('KABUPATEN ')) {
    const kabName = 'Kab. ' + titleCased.replace(/^Kabupaten /i, '');
    if (TOKOPEDIA_FCITY_MAP[kabName]) {
      return TOKOPEDIA_FCITY_MAP[kabName];
    }
  }
  if (upper.startsWith('KOTA ')) {
    const kotaName = titleCased.replace(/^Kota /i, '');
    if (TOKOPEDIA_FCITY_MAP[kotaName]) {
      return TOKOPEDIA_FCITY_MAP[kotaName];
    }
  }

  // 4. Try without "Kab." prefix
  if (TOKOPEDIA_FCITY_MAP['Kab. ' + titleCased]) {
    return TOKOPEDIA_FCITY_MAP['Kab. ' + titleCased];
  }

  // 5. Fuzzy: find first key that contains the name or vice versa
  const lowerName = regionName.toLowerCase();
  for (const [key, value] of Object.entries(TOKOPEDIA_FCITY_MAP)) {
    if (key.toLowerCase() === lowerName || key.toLowerCase().includes(lowerName)) {
      return value;
    }
  }

  // 6. Fall back to province-level mapping
  if (provinceName) {
    return lookupTokopediaFcity(provinceName, null);
  }

  return null;
}
